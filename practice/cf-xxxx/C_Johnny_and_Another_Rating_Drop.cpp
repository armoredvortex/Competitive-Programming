void solve()
{
    ll n;
    cin >> n;

    ll ans = 0;
    ll k = 64 - __countl_zero((unsigned long long)n);

    for (ll i = 0; i < k; i++)
    {
        // cerr << n << ' ' << (2 << i) << '\n';
        ans += n / (1ll << i);
    }
    cout << ans << '\n';
}

